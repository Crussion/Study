# 리스트에 문자열 저장
str_list = ['국어', '영어', '수학', '사회', '한국사']
print(str_list)
print(type(str_list))
print('-' * 20)

# 리스트에 정수 저장
num_list = [1, 2, 3, 4, 5]
print(num_list)
print(type(str_list))
print('-' * 20)

# 리스트에 실수 저장
float_list = [5.5, 3.14, 7.7]
print(float_list)
print('-' * 20)

# 리스트에 여러 자료형 저장
