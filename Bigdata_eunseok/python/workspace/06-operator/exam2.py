# 정수의 나눗셈
a = 7
b = 3
result = a / b
print(a, '/', b, '=', result)
print('-' * 20)

# 실수의 나눗셈
a = 7.0
b = 3.0
result = a / b
print(a, '/', b, '=', result)
print('-' * 20)

# 정수와 실수의 계산
a = 7
b = 3.0
result = a / b
print(a, '/', b, '=', result)
print('-' * 20)

# 나머지 계산 : 가능한 정수만 사용
a = 7.7777
b = 3.3
result = a % b
print(a, '%', b, '=', result)
print('-' * 20)

# 0으로 나누기 : 정수든, 실수든 에러 발생
a = 7
b = 0.0
result = a / b
print(a, '/', b, '=', result)
print('-' * 20)










