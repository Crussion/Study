s = 100
print(s)
print('-' * 20)

s += 100   # s = s + 100
print(s)
print('-' * 20)

s -= 100   # s = s - 100
print(s)
print('-' * 20)

s *= 100   # s = s * 100
print(s)
print('-' * 20)

s /= 100   # s = s / 100
print(s)
print('-' * 20)

s **= 100   # s = s ** 100
print(s)
print('-' * 20)

s //= 100   # s = s // 100
print(s)
print('-' * 20)

s %= 100   # s = s % 100
print(s)
print('-' * 20)
