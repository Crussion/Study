
public class Exam3 {
	public static void main(String[] args) {
		int n1 = 100;
		System.out.println(n1);
		
		n1 = n1 + 100;
		System.out.println(n1);
		
		n1 += 100;
		System.out.println(n1);
		
		n1 -= 100;
		System.out.println(n1);
		
		n1 *= 100;
		System.out.println(n1);
		
		n1 /= 100;
		System.out.println(n1);
		
		n1 %= 100;
		System.out.println(n1);
	}
}
