
public class Exam4 {
	public static void main(String[] args) {
		int num = 1;
		
		System.out.println(num);
		num = num + 1;
		System.out.println(num);
		num += 1;
		System.out.println(num);
		num++;
		System.out.println(num);
		++num;
		System.out.println(num);
		
		System.out.println(num);
		num = num - 1;
		System.out.println(num);
		num -= 1;
		System.out.println(num);
		num--;
		System.out.println(num);
		--num;
		System.out.println(num);
	}
}
